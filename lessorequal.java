
import java.util.*;

public class lessorequal {
    public static void main(String arg[]) {
    Scanner s = new Scanner(System.in);
    int a = s.nextInt();
    int b = s.nextInt();
    if (a == b) {
        System.out.println("equal");
    } else if (a > b) {
            System.out.println("a is greater ");
        }else {
            System.out.println("a is lesser ");
        }
     }
}

    
